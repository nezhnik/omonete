-- Суффикс каталожного номера ЦБ: последние две цифры года (5111-0178-26 → 26 = 2026).
-- Используется для определения года тиража при отображении и сортировке.
ALTER TABLE coins
  ADD COLUMN catalog_suffix VARCHAR(8) DEFAULT NULL COMMENT 'Суффикс каталога: год тиража, напр. 26 для 5111-0178-26' AFTER catalog_number;
